export class ComponentRegistryEngine {
  private static cache: ComponentGuideEntry[] | null = null;

  static all(): ComponentGuideEntry[] {
    if (!ComponentRegistryEngine.cache) {
      ComponentRegistryEngine.cache = [
        ...CORE,
        ...ADVANCED,
        ...GENERIC_IDS.map(([id, title, tel]) => genericEntry(id, title, tel)),
      ];
    }
    return ComponentRegistryEngine.cache;
  }

  static get(id: string): ComponentGuideEntry | null {
    return ComponentRegistryEngine.all().find((e) => e.id === id) ?? null;
  }

  /** Always returns a guide entry — creates on-the-fly copy for unknown panel ids. */
  static resolve(id: string, title?: string, telemetry?: string): ComponentGuideEntry {
    const existing = ComponentRegistryEngine.get(id);
    if (existing) return existing;
    const label =
      title ??
      id
        .replace(/([a-z])([A-Z])/g, "$1 $2")
        .replace(/desk$/i, " Desk")
        .replace(/-/g, " ")
        .replace(/\b\w/g, (c) => c.toUpperCase());
    return genericEntry(id, label, telemetry ?? "SYS");
  }

  static search(query: string): ComponentGuideEntry[] {
    const q = query.trim().toLowerCase();
    if (!q) return ComponentRegistryEngine.all();
    return ComponentRegistryEngine.all().filter(
      (e) =>
        e.id.includes(q) ||
        e.title.toLowerCase().includes(q) ||
        e.telemetry.toLowerCase().includes(q) ||
        e.operationalExplanation.toLowerCase().includes(q),
    );
  }
}

import type { ComponentGuideEntry } from "@/types/operator-guide";

function entry(
  id: string,
  title: string,
  telemetry: string,
  operational: string,
  why: string,
  professional: string,
  interpretation: string,
  related: string[],
  workflows: string[],
  shortcuts: string[],
  useCases: string[],
  metrics: string[],
): ComponentGuideEntry {
  return {
    id,
    title,
    telemetry,
    operationalExplanation: operational,
    whyItMatters: why,
    professionalUsage: professional,
    marketInterpretation: interpretation,
    relatedSystems: related,
    workflowConnections: workflows,
    shortcuts,
    useCases,
    relatedMetrics: metrics,
  };
}

